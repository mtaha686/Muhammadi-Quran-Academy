    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const statisticBoxes = document.querySelectorAll('.statistic .box .stat');
    
            function incrementStatisticValue(element, targetValue) {
                let currentValue = 0;
                const increment = Math.ceil(targetValue / 1000);
    
                const intervalId = setInterval(() => {
                    currentValue += increment;
                    if (currentValue >= targetValue) {
                        currentValue = targetValue;
                        clearInterval(intervalId);
                    }
                    element.textContent = currentValue;
                }, 10);
            }
    
            statisticBoxes.forEach(box => {
                const targetValue = parseInt(box.textContent);
                box.textContent = '0';
                incrementStatisticValue(box, targetValue);
            });
        });
    </script>
    
